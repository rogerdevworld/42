#include <stdio.h>
#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len] != '\0')
		len++;
	return (len);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	size_t	len;
	char	*duplicado;

	len = ft_strlen(src);
	duplicado = (char *)malloc((len + 1) * sizeof(char));
	if (!duplicado)
		return (NULL);
	ft_strcpy(duplicado, src);
	return (duplicado);
}
